-- 13.Calculate the average order value for each customer.
WITH customer_totals AS
	(SELECT 
		c.customerid,
		CONCAT(c.firstname, ' ', c.lastname) AS customername,
		COUNT(o.orderid) order_count,
		SUM(totalamount) AS lifetime_spending
	FROM
		customers c
			JOIN
		orders o ON c.customerId = o.customerid
	GROUP BY c.customerid
)

SELECT 
	customerid, 
	customername,
	order_count, 
	lifetime_spending,
	lifetime_spending/ order_count AS avg_order_value
from customer_totals;


-- 14.List customers who have placed orders in more than one store.
SELECT 
    c.customerid,
    c.firstname,
    c.lastname,
    COUNT(DISTINCT o.storeid) AS stores_visited
FROM
    customers c
        JOIN
    orders o ON c.customerid = o.customerid
