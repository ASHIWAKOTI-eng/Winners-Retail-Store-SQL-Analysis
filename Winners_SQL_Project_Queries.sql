-- 1.List all orders along with the customer name, store name, and employee who handled the order.
SELECT 
    o.OrderID,
    CONCAT(c.FirstName, ' ', c.LastName) AS CustomerName,
    s.StoreName,
    CONCAT(e.FirstName, ' ', e.LastName) AS EmployeeName
FROM
    orders o
        JOIN
    customers c ON o.CustomerID = c.CustomerID
        JOIN
    employees e ON o.EmployeeID = e.EmployeeID
        JOIN
    stores s ON o.StoreID = s.StoreID;
    
-- 2.Show each product sold along with its category and the total quantity sold. 
SELECT 
    p.ProductName,
    p.CategoryID,
    SUM(od.Quantity) AS Total_Quantity_sold,
    c.CategoryName
FROM
    order_details od
        JOIN
    products p ON od.ProductID = p.ProductID
        JOIN
    categories c ON p.CategoryID = c.CategoryID
GROUP BY p.ProductID;


-- 3.List all customers who have placed at least one order, showing their orders and total spent.
SELECT 
    o.CustomerID,
    CONCAT(c.FirstName, ' ', c.LastName) AS CustomerName,
    SUM(o.TotalAmount) AS Total_spent
FROM
    orders o
        JOIN
    customers c ON o.CustomerID = c.CustomerID
GROUP BY c.CustomerID;

-- 4.Identify orders where the employee’s store is different from the store where the order was placed.
SELECT 
    o.OrderID,
    o.EmployeeID,
    o.StoreID AS Order_Store,
    e.StoreID AS Employee_store
FROM
    orders o
        JOIN
    employees e ON o.EmployeeID = e.EmployeeID
WHERE
    o.storeID != e.StoreID;


-- 5.Show the top 10 customers by total spending across all orders.
