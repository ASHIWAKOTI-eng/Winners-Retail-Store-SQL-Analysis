-- 18.Calculate total revenue generated per product.
SELECT 
    p.productid,
    p.productname,
    SUM(od.quantity) AS quantity_sold,
    ROUND(SUM(od.unitprice * od.quantity * (1 - od.discount / 100)),
            2) AS total_product_revenue
FROM
    products p
        JOIN
    order_details od ON p.productid = od.productid
GROUP BY p.productid , p.productname;


-- 19.Determine the average quantity sold per order for each product.
SELECT 
    od.productID,
    COUNT(o.orderId) AS order_count,
    SUM(od.quantity) AS total_units_sold,
    ROUND(SUM(od.quantity) / COUNT(o.orderID), 2) AS avg_quantity_sold_per_order
FROM
    orders o
        JOIN
    order_details od ON o.orderID = od.orderID
GROUP BY od.productID;

-- 20.Identify products with total sales revenue greater than the average product revenue.
WITH total_revenue AS (
	SELECT
		productID,
		round(sum(quantity * unitprice * (1-discount/100)), 2) AS product_revenue,
		AVG(sum(quantity * unitprice * (1-discount/100))) OVER () AS avg_revenue
	FROM order_details
	GROUP BY productID
    )
    
 SELECT
	productID,
    product_revenue,
    avg_revenue
FROM total_revenue
WHERE product_revenue > avg_revenue;

